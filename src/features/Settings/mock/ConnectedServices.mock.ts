export const getConnectedServicesMock = (params: GetUserConsentAPIParamsInterface): GetUserConsentAPIResponse => {
  const start = Number(params.Offset) * Number(params.PageSize);
  const end = start + Number(params.PageSize);
  const currentDate = new Date();

  const totalRecords = connectedServices.filter(service =>
    params.Status.toLowerCase().split(",").includes(service.ConsentStatus.toLowerCase())
  );

  const connectedAccounts = connectedServices
    .filter(service => params.Status.toLowerCase().split(",").includes(service.ConsentStatus.toLowerCase()))
    .filter(service => {
      if (params.ToCreationDate) {
        const diffInMonths =
          (currentDate.getTime() - new Date(service.CreationDateTime).getTime()) / (1000 * 60 * 60 * 24 * 30); // Approximate number of days in a month

        switch (params.ToCreationDate) {
          case "1":
            return diffInMonths <= 1;
          case "3":
            return diffInMonths <= 3;
          case "6":
            return diffInMonths <= 6;
          case "12":
            return diffInMonths <= 12;
          case "more":
            return true; // Return true for "more" case
        }
      }
      return true;
    })
    .filter(service => {
      if (params.TPPId) {
        if (params.TPPId !== service.TPPInfo.TPPId) {
          return false;
        }
      }
      return true;
    })
    .slice(start, end);
  return { connectedAccounts, totalRecords: totalRecords.length };
};

