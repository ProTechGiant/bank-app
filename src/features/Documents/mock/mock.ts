function getRandomDate() {
  const currentDate = new Date();

  // Subtract a random number of hours between 0 and 22 from the current date
  const randomHours = Math.floor(Math.random() * 22);
  const randomDate = sub(currentDate, { hours: randomHours });

  return format(randomDate, "dd-MM-yyyy hh:mm:ss a");
}

